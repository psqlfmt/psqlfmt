use std::collections::HashSet;

use once_cell::sync::Lazy;

pub static KEYWORDS: Lazy<HashSet<&'static str>> = Lazy::new(|| {
    keyword_slice()
        .iter()
        .copied()
        .chain(SQL_STANDARD_EXTRAS.iter().copied())
        .collect()
});

pub static TYPES: Lazy<HashSet<&'static str>> =
    Lazy::new(|| TYPE_WORDS.iter().copied().collect::<HashSet<_>>());

pub static FUNCTIONS: Lazy<HashSet<&'static str>> =
    Lazy::new(|| FUNCTION_WORDS.iter().copied().collect::<HashSet<_>>());

pub static CLAUSE_KEYWORDS: Lazy<HashSet<&'static str>> =
    Lazy::new(|| CLAUSE_WORDS.iter().copied().collect::<HashSet<_>>());

pub fn is_keyword(word: &str) -> bool {
    KEYWORDS.contains(word.to_ascii_uppercase().as_str())
}

pub fn is_type(word: &str) -> bool {
    TYPES.contains(word.to_ascii_uppercase().as_str())
}

pub fn is_function(word: &str) -> bool {
    FUNCTIONS.contains(word.to_ascii_lowercase().as_str())
}

pub fn is_clause_keyword(word: &str) -> bool {
    CLAUSE_KEYWORDS.contains(word.to_ascii_uppercase().as_str())
}

